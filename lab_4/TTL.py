#..............................................
def stat():
  N = len(UKLAD)
  t = {}
  for i in range(N):
    t.setdefault(UKLAD[i][0],0)
    t[UKLAD[i][0]]+=1

  print("Bramki:")
  for br in ["AND","NAND","OR","NOR","XOR","XNOR","NOT","BUF"]:
    if t.get(br,0)>0: print(f"  {br:5}: {t[br]}")

